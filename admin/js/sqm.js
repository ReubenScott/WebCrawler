function core_hmac_md5(key, data) {
    var bkey = str2binl(key);
    if (bkey.length > 0x10) bkey = core_md5(bkey, key.length * chrsz);
    var ipad = Array(0x10),
        opad = Array(0x10);
    for (var i = 0x0; i < 0x10; i++) {
        ipad[i] = bkey[i] ^ 0x36363636;
        opad[i] = bkey[i] ^ 0x5C5C5C5C
    };
    var hash = core_md5(ipad.concat(str2binl(data)), 0x200 + data.length * chrsz);
    return core_md5(opad.concat(hash), 0x200 + 0x80)
};

function safe_add(x, y) {
    var lsw = (x & 0xFFFF) + (y & 0xFFFF);
    var msw = (x >> 0x10) + (y >> 0x10) + (lsw >> 0x10);
    return (msw << 0x10) | (lsw & 0xFFFF)
};

function bit_rol(num, cnt) {
    return (num << cnt) | (num >>> (0x20 - cnt))
};

